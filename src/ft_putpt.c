#include "fillit.h"
#include "libft.h"

void	ft_putpt(t_pt p)
{
	ft_putchar('(');
	ft_putnbr(p.x);
	ft_putchar(',');
	ft_putnbr(p.y);
	ft_putchar(')');
}
